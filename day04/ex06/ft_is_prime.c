int		ft_is_prime(int nb);

int		ft_is_prime(int nb)
{
	int i;

	i = 3;
	if (nb == 1 || nb == 0)
		return (0);
	if (nb == 2 || nb == 3 || nb == 5)
		return (1);
	if (nb % 2 == 0 || nb % 3 == 0 || nb % 5 == 0)
		return (0);
	while (i <= nb / 2)
		if (nb % i == 0)
			return (0);
		else
			i++;
	return (1);
}
