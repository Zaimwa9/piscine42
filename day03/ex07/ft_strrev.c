char	*ft_strrev(char *str)
{
	int		len;
	int		i;
	char	swap;

	len = 0;
	i = 0;
	while (str[i] != '\0')
	{
		i++;
		len++;
	}
	i = 0;
	while (i < len / 2)
	{
		swap = *(str + i);
		*(str + i) = *(str + (len - i - 1));
		*(str + (len - i - 1)) = swap;
		i++;
	}
	return (str);
}
